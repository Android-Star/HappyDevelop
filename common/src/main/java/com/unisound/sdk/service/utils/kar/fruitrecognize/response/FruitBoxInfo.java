package com.unisound.sdk.service.utils.kar.fruitrecognize.response;

import java.io.Serializable;

public class FruitBoxInfo implements Serializable {
  private int w;
  private int x;
  private int h;
  private int y;

  public int getW() {
    return w;
  }

  public void setW(int w) {
    this.w = w;
  }

  public int getX() {
    return x;
  }

  public void setX(int x) {
    this.x = x;
  }

  public int getH() {
    return h;
  }

  public void setH(int h) {
    this.h = h;
  }

  public int getY() {
    return y;
  }

  public void setY(int y) {
    this.y = y;
  }
}
